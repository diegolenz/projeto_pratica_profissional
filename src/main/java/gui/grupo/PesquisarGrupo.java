    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            grupos = grupoService.getAll(edtpesquisa.getText());
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        tableModelGrupo.setList(this.grupos.toArray());
        tabela.setModel(tableModelGrupo);
        if (grupos.isEmpty()) {
            if (evt != null)
                JOptionPane.showMessageDialog(this, "Nenhum resultado foi retornado");
        }else
            tabela.setRowSelectionInterval(0,0);
    }

    private Integer getPosicao(Grupo marca){
        try {
            if (marca.getId() == null)
                marca = new GrupoService().getLast();
        } catch (Exception e) {
            e.printStackTrace();
        }

        Integer tam = grupos.size();
        Integer pos = 0;
        boolean achou =false;
        while ( pos < tam && !achou ){
            if (grupos.get(pos).getId().equals(marca.getId())) {
                achou =true;
            } else
                pos++;
        }
        return pos;
    }

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {
        CadastrarGrupoForm.Callback callback = (marca -> {
            try {
                grupos = new GrupoService().getAll("");
            } catch (Exception ex){
                System.out.println(ex);
            }
            tableModelGrupo.setList(grupos.toArray());

            if (!grupos.isEmpty()) {
                tabela.setRowSelectionInterval(getPosicao(marca), getPosicao(marca));
            }
        });
        cadastrarGrupoForm  = new CadastrarGrupoForm(this, true, new Grupo(), callback);
        cadastrarGrupoForm.desbloqueiaedt();
        cadastrarGrupoForm.show();
    }

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {
        if (tabela.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um registro para continuar");
            return;
        }
        CadastrarGrupoForm.Callback callback = (marca -> {
            try {
                grupos = new GrupoService().getAll("");
            } catch (Exception ex){
                System.out.println(ex);
            }
            tableModelGrupo.setList(grupos.toArray());

            if (!grupos.isEmpty()) {
                tabela.setRowSelectionInterval(getPosicao(marca), getPosicao(marca));
            }
        });
        grupoSelecionado=grupos.get(tabela.getSelectedRow());
        cadastrarGrupoForm=new CadastrarGrupoForm(this,false, grupoSelecionado,callback);
        cadastrarGrupoForm.setGrupo(this.grupoSelecionado);
        cadastrarGrupoForm.carregaEdt();
        cadastrarGrupoForm.desbloqueiaedt();
        cadastrarGrupoForm.show();
    }

