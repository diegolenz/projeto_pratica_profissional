    private void btnExcluirDesativarActionPerformed(ActionEvent evt) {
        if (tabela.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um registro para continuar");
            return;
        }
        grupoSelecionado=grupos.get(tabela.getSelectedRow());
        CadastrarGrupoForm cadastrarGrupoForm = new CadastrarGrupoForm(getWindowParent(), true, grupoSelecionado, null);
        cadastrarGrupoForm.carregaEdt();
        cadastrarGrupoForm.bloqueiaedt();
        cadastrarGrupoForm.show();
        try {
            grupoService.deleteByID(grupoSelecionado.getId());
            if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o grupo selecionado ?", "Atenção", JOptionPane.YES_NO_OPTION) != 0) {
                cadastrarGrupoForm.dispose();
                return;
            }
            JOptionPane.showMessageDialog(this, "Excluido com sucesso");

        } catch (Exception e) {
            if (JOptionPane.showConfirmDialog(this,"Não é possivel excluir o registro, deseja desativa-lo?", "ATENÇÂO", JOptionPane.YES_NO_OPTION)==0) {
                if (!grupoSelecionado.getAtivo())
                    JOptionPane.showMessageDialog(this, "Pessoa já está desativada");
                try {
                    grupoService.update(grupoSelecionado);
                    JOptionPane.showMessageDialog(this,
                            "Desativado com sucesso"
                    );
                    grupos = new GrupoService().getAll("");
                } catch (Exception e1) {
                    JOptionPane.showMessageDialog(this, "Não foi possivel desativar");
                }
            }
        }
        try {
            grupos = new GrupoService().getAll("");
        } catch (Exception ex){}
        tableModelGrupo.setList(grupos.toArray());
        tabela.setModel(tableModelGrupo);
        cadastrarGrupoForm.dispose();
    }



