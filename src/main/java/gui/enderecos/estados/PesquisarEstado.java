    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            if (this.pais == null)
                list = estadoService.getAll(edtpesquisa.getText());
            else
                list = estadoService.getByPais(pais.getId());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,  e.getMessage());
        }
        modelo.setList(list.toArray());
        tabela.setModel(modelo);
        if (list.isEmpty() && evt != null ){
            JOptionPane.showMessageDialog(this, "Nenhum resultado encontrado");
        } else if (!list.isEmpty()){
            tabela.setRowSelectionInterval(0,0);
        }
    }

    private void btnselecionarActionPerformed(java.awt.event.ActionEvent evt) {
        if (tabela.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this , "Nenhum estado selecionada");
            return;
        } else {
            Integer id=(Integer)this.tabela.getValueAt(this.tabela.getSelectedRow(),0);
            try {
                estadoSelecionada = estadoService.getEstadoByID(id);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            if (!estadoSelecionada.getAtivo()){
                JOptionPane.showMessageDialog(this,"Estado selecionado não esta ativo");
                return;
            }
            callback.handle(estadoSelecionada);
            this.dispose();
        }
    }

