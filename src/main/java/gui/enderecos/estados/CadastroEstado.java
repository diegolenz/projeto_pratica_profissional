    private void edtCoPaisKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtCoPaisKeyPressed
        if (evt.getKeyCode() == KeyCode.ENTER.impl_getCode()) {
            try {
                PaisService paisService =new PaisService();
                this.estado.setPais( paisService.getPaisByID(Integer.parseInt(edtCoPais.getText())));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            if (  this.estado.getPais() != null){
              this.edtPais.setText(estado.getPais().getNome());
            }
        }
    }//GEN-LAST:event_edtCoPaisKeyPressed

