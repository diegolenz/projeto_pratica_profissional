    private void btnAltPAisActionPerformed(java.awt.event.ActionEvent evt) {
        PesquisarPais.Callback callback = (p) ->{
            if (p==null){
                return;
            }
            this.estado.setPais(p);
            this.edtPais.setText(p.getNome());
        };
       new PesquisarPais(this, true, callback).show();
    }

    public void carregaEdt(){
        edtUF.setText(estado.getSigla());
        edtCod.setText(estado.getId().toString());
        edtNome.setText(estado.getNome());
        edtCoPais.setText(estado.getPais().getId().toString());
        edtPais.setText(estado.getPais().getNome());
        rdAtivado.setSelected(estado.getAtivo());
        rdDesativado.setSelected(!estado.getAtivo());
        if (this.estado.getId()==null) {
            rdAtivado.setEnabled(false);
            rdDesativado.setEnabled(false);
            rdAtivado.setSelected(true);
        }

    }

    public void bloqueiaEdt(){
        this.edtCoPais.setEditable(false);
        this.edtNome.setEditable(false);
        this.edtPais.setEditable(false);
        this.edtUF.setEditable(false);
        this.btnAltPAis.setEnabled(false);
        rdDesativado.setEnabled(false);
        rdAtivado.setEnabled(false);
        btnAltPAis.setVisible(false);
        btnCancelar.setVisible(false);
        btnSalvar.setVisible(false);
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {
        estado.setNome(edtNome.getText());
        estado.setSigla(edtUF.getText());
        if (estado.getNome().trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"Campo nome não informado");
            this.edtNome.requestFocus();
            return;
        }
        if (estado.getPais() == null){
            JOptionPane.showMessageDialog(this,"País não informado");
            this.edtCoPais.requestFocus();
            return;
        }
        if (estado.getId()==null){
            try {
                estado.setAtivo(true);
                estadoService.save(estado);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao salvar : " + e.getMessage());
                return;
            }
        } else {
            try {
                if (rdAtivado.isSelected())
                    estado.setAtivo(true);
                else
                    estado.setAtivo(false);
                estadoService.update(estado);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao salvar : "+ e.getMessage());
                return;
            }
        }

        JOptionPane.showMessageDialog(this, "Salvo com sucesso");
        callback.handle(estado);
        dispose();
    }                                         

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {
        if (JOptionPane.showConfirmDialog(this, "nenhum dado alterado será salvo, deseja mesmo cancelar?","CANCELAR", JOptionPane.YES_NO_OPTION)==0){
            dispose();
        };
    }                                           

