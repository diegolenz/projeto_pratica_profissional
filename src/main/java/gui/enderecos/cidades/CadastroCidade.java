    public interface Callback {
        void handle(Cidade cidade);
    }

    public void carregaEdt(){
        rdAtivado.setSelected(cidade.getAtivo());
        rdDesativado.setSelected(!cidade.getAtivo());
        edtcod.setText(cidade.getId().toString());
        edtDDD.setText(cidade.getDDD());
        edtnome.setText(cidade.getNome());
        edtCodEstado.setText(cidade.getEstado().getId().toString());
        edtEstado.setText(cidade.getEstado().getNome());
    }

    public void bloqueiaEdt(){
        rdAtivado.setEnabled(false);
        rdDesativado.setEnabled(false);
        edtcod.setEnabled(false);
        edtDDD.setEnabled(false);
        edtnome.setEnabled(false);
        edtCodEstado.setEnabled(false);
        edtEstado.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnestado.setEnabled(false);
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        cidade.setDDD(edtDDD.getText());
        cidade.setNome(edtnome.getText());

        if (cidade.getNome().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo cidade não informado");
            edtnome.requestFocus();
            return;
        }
        if (cidade.getEstado() == null){
            JOptionPane.showMessageDialog(this, "Estado não informado");
            edtCodEstado.requestFocus();
            return;
        }

        if (cidade.getId()==null){
            cidade.setAtivo(true);
            try {
                cidadeService.save(cidade);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao salvar dados : "+ e.getMessage());
                return;
            }
        } else {
            try {
                cidadeService.update(cidade);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Falha ao salvar : "+e.getMessage());
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Salvo com sucesso");
        dispose();
        callback.handle(cidade);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {                                            
        if(JOptionPane.showConfirmDialog(this, "nenhum dado alterado será salvo, deseja mesmo cancelar?","CANCELAR", JOptionPane.YES_NO_OPTION)==0){
            dispose();
        };
    }

    private void btnestadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnestadoActionPerformed
        PesquisarEstado.Callback callback = (p) ->{
            if (p==null){
                return;
            }
            this.cidade.setEstado(p);
            this.edtCodEstado.setText(p.getPais().getId().toString());
            this.edtEstado.setText(p.getNome());
            this.edtCodEstado.setText(p.getId().toString());
        };
        new PesquisarEstado(this, true, callback).show();
    }//GEN-LAST:event_btnestadoActionPerformed

