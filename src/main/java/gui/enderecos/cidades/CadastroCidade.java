    private void edtCodEstadoKeyPressed(java.awt.event.KeyEvent evt) {
        if (evt.getKeyCode() == KeyCode.ENTER.impl_getCode()) {
            Estado estado =null;
            try {
                estado = this.estadoService.getEstadoByID(Integer.parseInt(edtCodEstado.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
                return;
            }
            if (estado==null) {
                JOptionPane.showMessageDialog(this, "Nenhum Estado encontrado");
                return;
            }
            this.cidade.setEstado(estado);
            this.edtEstado.setText(this.cidade.getEstado().getNome());
        }
    }

