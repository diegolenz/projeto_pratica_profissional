    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisarActionPerformed
        try {
            list= paisService.getAllAtivos(edtpesquisa.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Não foi possivel o retorno dos dados : "+e.getMessage());
            return;
        }
        modelo.setList(list.toArray());
       tabela.setModel(modelo);
       if (list.isEmpty()){
           if (evt !=null)
           JOptionPane.showMessageDialog(this,"Nenhum resultado encontrado");
           return;
       } else this.tabela.setRowSelectionInterval(0,0);
    }//GEN-LAST:event_btnpesquisarActionPerformed

    private void btnselecionarActionPerformed(java.awt.event.ActionEvent evt) {
        if (tabela.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this , "Nenhuma pessoa selecionada");
            return;
        } else {
            paisSelecionada=list.get(tabela.getSelectedRow());
            if (!paisSelecionada.getAtivo()){
                JOptionPane.showMessageDialog(this,"País selecionado não esta ativo");
                return;
            }
            callback.handle(paisSelecionada);
            this.dispose();
        }
    }

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CadastroPais.Callback callback = (produto -> {
            try {
                list = new  PaisService().getAll("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Falha ao retornar dados");
            }
            Integer pos = list.indexOf(produto);
            tabela.setRowSelectionInterval(pos,pos);
        });
        new  CadastroPais(this, true, new Pais(), callback).show();
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (tabela.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um registro para continuar");
            return;
        }
        CadastroPais.Callback callback = (produto -> {
            try {
                list = new  PaisService().getAll("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Falha ao retornar dados");
            }
            Integer pos = list.indexOf(produto);
            tabela.setRowSelectionInterval(pos,pos);
        });
        Pais paisSelecionado=list.get(tabela.getSelectedRow());
        cadastroPais=new CadastroPais(this, true, paisSelecionado, callback);
        cadastroPais.carregaEdt();
        cadastroPais.show();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tabela.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um registro para continuar");
            return;
        }
        Pais paisSelecionado=list.get(tabela.getSelectedRow());
        try {
            paisService.deleteByID(paisSelecionado.getId());
            JOptionPane.showMessageDialog(this, "Excluido com sucesso");
            return;

        }catch (Exception e) {
            if (JOptionPane.showConfirmDialog(this,"Não é possivel excluir o registro, deseja desativa-lo?", "ATENÇÂO", JOptionPane.YES_NO_OPTION)==0) {
                if (!paisSelecionado.getAtivo())
                    JOptionPane.showMessageDialog(this, "Pais já esta desativado");
                try {
                    paisService.update(paisSelecionado);
                    JOptionPane.showMessageDialog(this,
                            "Desativado com sucesso"
                    );
                    return;
                } catch (Exception e1) {
                    JOptionPane.showMessageDialog(this, "Não foi possivel desativar");
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

