    private void btnReceberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceberActionPerformed
        if (edtDataPagamento.getDate() == null){
            JOptionPane.showMessageDialog(this,"Data invalida");
            return;
        }
        try {
            contaPagar.setDataPagamento(edtDataPagamento.getDate());
            contaPagar.setPaga(true);
            new ContasPagarDao().update(contaPagar);
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(this, "Falha ao receber \n" + ex.getMessage() );
            return;
        }
        JOptionPane.showMessageDialog(this, "Pago com sucesso");
        dispose();
    }//GEN-LAST:event_btnReceberActionPerformed

