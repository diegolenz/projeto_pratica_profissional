    private void btnPesquisaarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaarActionPerformed
        try {
            caixas = new CaixaService().getAll(edtPesquisa.getText());
            tableModelCaixas.setList(caixas.toArray());
            jTable1.setModel(tableModelCaixas);
            if (!caixas.isEmpty())
                jTable1.setRowSelectionInterval(0, 0);
            else if (evt != null)
                new AvisoForm().aviso("Nenhum resultado encontrado", "Atenção", getWindowParent(), 2);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Falha ao retornar dados");
        }
    }//GEN-LAST:event_btnPesquisaarActionPerformed


