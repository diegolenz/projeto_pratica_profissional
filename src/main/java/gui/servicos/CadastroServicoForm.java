    public void carregaEdt(){
        edtcodigo.setText(Integer.toString(servico.getId()));
        edtdescricao.setText(servico.getNome());
        edtdtcad.setText(servico.getDt_cadastro().toString());
        edtdtultalteracao.setText(servico.getDt_ult_alteracao().toString());
        edtprecovenda.setValue(servico.getValor());
        this.edtcodigo.setText(servico.getId().toString());
        this.edtGrupo.setText(servico.getGrupo().getNome());
        this.edtCodGrupo.setText(servico.getGrupo().getId().toString());
    }

    public void carregar(){
        servico.setDt_ult_alteracao(new Date(System.currentTimeMillis()));
        if (servico.getId()==null){
            servico.setDt_cadastro(new Date(System.currentTimeMillis()));
            servico.setQtdVend(0);
            servico.setAtivo(true);
        }
        servico.setNome(edtdescricao.getText());
        servico.setValor((Double) edtprecovenda.getValue());
    }

    public void bloqueiaEdt(){
        edtprecovenda.setEditable(false);
        edtdtcad.setEditable(false);
        edtdtultalteracao.setEditable(false);
        edtcodigo.setEditable(false);
        edtdescricao.setEditable(false);
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt)  {
        this.carregar();
        if (servico.getNome().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Serviço não informado");
            this.edtdescricao.requestFocus();
            return;
        }
        if (servico.getGrupo() == null){
            JOptionPane.showMessageDialog(this, "Grupo não informado");
            this.edtCodGrupo.requestFocus();
            return;
        }

        try {
            if (servico.getId()==null)
                new ServicoService().save(servico);
            else {
                new ServicoService().update(servico);
            }
            JOptionPane.showMessageDialog(this, "Serviço salvo com sucesso!");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Falha ao salvar : "+ e.getMessage());
        }


    }

