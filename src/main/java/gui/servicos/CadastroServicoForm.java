    private void edtCodGrupoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtCodGrupoKeyPressed
        if (evt.getKeyCode() == KeyCode.ENTER.impl_getCode()) {
            try {
                GrupoService grupoService = new GrupoService();
                this.servico.setGrupo((Grupo) grupoService.getByID(Integer.parseInt(edtCodGrupo.getText())));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            if (this.servico.getGrupo() != null) {
                this.edtGrupo.setText(servico.getGrupo().getNome());
                this.edtCodGrupo.setText(servico.getGrupo().getId().toString());
            }
        }
    }//GEN-LAST:event_edtCodGrupoKeyPressed

    private void btnAlterarGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarGrupoActionPerformed
        PesquisarGrupo.Callback callback = (grupoRetorno) -> {
            if (grupoRetorno == null) {
                return;
            }
           this.servico.setGrupo(grupoRetorno);
            this.edtGrupo.setText(grupoRetorno.getNome());
            this.edtCodGrupo.setText(servico.getGrupo().getId().toString());
        };
        PesquisarGrupo pesquisarMarca = new PesquisarGrupo(this, true, callback);
        pesquisarMarca.show();
    }//GEN-LAST:event_btnAlterarGrupoActionPerformed

