    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        VendaProduto vendaSelecionada = vendas.get(this.jTable1.getSelectedRow());
        if (!vendaSelecionada.isAtivo()){
            JOptionPane.showMessageDialog(getWindowParent(), "Venda ja cancelada");
            return;
        }
        CadastroVendaProdutoForm cadastroVendaForm = new CadastroVendaProdutoForm(getWindowParent(), true, vendaSelecionada);
        cadastroVendaForm.carregar();
        cadastroVendaForm.bloqueiaEDT();
        cadastroVendaForm.show();
        if (JOptionPane.showConfirmDialog(getWindowParent(), "A operação de cancelamento da venda resultará no etorno dos operações financeiras e de estoque \n Deseja realmente cancelar a venda selecionada ?",
                "Atenção", JOptionPane.YES_NO_OPTION)==0){
            String motivo = JOptionPane.showInputDialog(getWindowParent(), "Informe um motivo para o cancelamento com no minimo 10 caracteres");
            if (motivo.trim().isEmpty()){
                JOptionPane.showMessageDialog(getWindowParent(), "Operação de cancelamento de venda não cocluido \n Motivo informado invalido");
                cadastroVendaForm.dispose();
                return;
            }
            vendaSelecionada.setMotivoCancelamento(motivo);
            vendaSelecionada.setAtivo(false);
            try {
                new VendaProdutoService().cancelamento(vendaSelecionada);
                JOptionPane.showMessageDialog(this, "Venda cancelada com sucesso");
                cadastroVendaForm.dispose();
                return;
            } catch (SQLException e){
                JOptionPane.showMessageDialog(this, "Falha ao cancelar venda: " + e.getMessage() + "\n causa : " +e.getCause());
                cadastroVendaForm.dispose();
                return;
            }

        }
        JOptionPane.showMessageDialog(getWindowParent(), "Operação de cancelamento de venda cancelada");
        cadastroVendaForm.dispose();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void BtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarActionPerformed
        try {
            vendas.clear();
            vendas.addAll(new VendaProdutoService().getAll(""));
        }catch (Exception e){
            JOptionPane.showMessageDialog(this,"Falha ao retornar dados" + e);
        }
        tableModelVenda.setList(vendas.toArray());
        jTable1.setModel(tableModelVenda);
        if (vendas.isEmpty()) {
            if (evt != null) {
                JOptionPane.showMessageDialog(this, "Nenhum resultado encontrado");
            }
        } else jTable1.setRowSelectionInterval(0,0);

    }//GEN-LAST:event_BtnPesquisarActionPerformed


